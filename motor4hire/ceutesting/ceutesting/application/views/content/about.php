<h3>Course Description</h3>
<p>This activity consists of a 55 minute instructional video, an accompanying library of associated reference material, a registration form, a post-test, and an evaluation form.  Upon successful completion of 80% on the post test, the participant will be awarded 3 contact hours in continuing nursing education and issued a certificate.</p>

<h3>Course Content</h3>
<p>The content is presented in two parts.  First is an Introduction to Alpha-1 Antitrypsin Deficiency.  Content includes the discovery, pathophysiology, genetics, risk factors, psychosocial implications, and diagnosis and treatment of this genetic disorder.  In the second part of the program, the nurse is provided with drug specific step-by-step instructions for preparing and infusing Prolastin-C Liquid. Additionally, the nurse is provided with patient assessment and documentation information as well as available community resources.</p>

<h3>Course Objectives</h3>

The Nurse will:
<ol>
<li>Participants will describe that Alpha-1 is genetically transmitted, can lead to liver disease, early onset emphysema and other conditions.</li>
<li>Participants will explain common barriers to the clinical diagnosis of Alpha-1.</li>
<li>Participants will list at least three key elements in the treatment of individuals with Alpha-1 including augmentation therapy with Prolastin C Liquid.</li>
<li>Participants will describe criteria for augmentation therapy with Prolastin C Liquid.</li>
<li>Participants will list at least 4 potential side effects associated with augmentation therapy with Prolastin-C Liquid.</li>
<li>Each participant shall identify procedures necessary for safe and effective Prolastin-C Liquid augmentation therapy.</li>
<li>Participants will name resources available in the community for those affected by Alpha-1 Antitrypsin Deficiency and their health care providers.</li>
</ol>

